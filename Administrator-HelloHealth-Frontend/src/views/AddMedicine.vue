import axios from "axios";
import {ElMessage} from "element-plus";

export default {
    name: "AddMedicineView",
    components: {
        UploadFilled
    },
    data() {
        return {
            isPrescription: false,
            isInsurance: false,
            medicine: {
                medicine_id: '',
                medicine_ch_name: '',
                medicine_en_name: '',
                medicine_category: '',
                medicine_abbreviation: '',
                medicine_introduction: '',
                medicine_country: '',
                medicine_manufacturer: '',
                medicine_form: '',
                medicine_character: '',
                medicine_ingredient: '',
                medicine_validityperiod: '',
                medicine_usage: '',
                medicine_indications: '',
                medicine_taboo: '',
                medicine_storage: '',
                is_prescription_medicine: false,
                is_medical_insurance_medicine: false,
                medicine_image: null,
                medicine_reference_quote: '',
            }
        }
    },
    methods: {
        checkRequired(){
            let flag = true;
            if(this.medicine.medicine_id === ''){
                ElMessage.error('药品批准文号不能为空');
                flag=false;
            }
            if(this.medicine.medicine_ch_name === ''){
                ElMessage.error('药品中文名不能为空');
                flag=false;
            }
            if(this.medicine.medicine_category === ''){
                ElMessage.error('药品分类不能为空');
                flag=false;
            }
            //药品来源国家
            if(this.medicine.medicine_country === ''){
                ElMessage.error('药品来源国家不能为空');
                flag=false;
            }
            //药品厂商
            if(this.medicine.medicine_manufacturer === ''){
                ElMessage.error('药品厂商不能为空');
                flag=false;
            }
            //药品剂型
            if(this.medicine.medicine_form === ''){
                ElMessage.error('药品剂型不能为空');
                flag=false;
            }
            //药品成分
            if(this.medicine.medicine_ingredient === ''){
                ElMessage.error('药品成分不能为空');
                flag=false;
            }
            //药品适应症
            if(this.medicine.medicine_indications === ''){
                ElMessage.error('药品适应症不能为空');
                flag=false;
            }
            //药品禁忌
            if(this.medicine.medicine_taboo === ''){
                ElMessage.error('药品禁忌不能为空');
                flag=false;
            }
            //药品参考报价
            if(this.medicine.medicine_reference_quote === ''){
                ElMessage.error('药品参考报价不能为空');
                flag=false;
            }
            console.log(flag);
            return flag;
        },
        submitNewInfo() {
            //当所有必填项都不为空时，才提交数据
            if(this.checkRequired()) {
                axios.post('/api/admin/medicine', this.medicine)
                    .then(response => {
                        if (response.data.data.state) {
                            console.log("Successfully submitted medicine data:", response.data.data.message);
                            console.log(this.medicine);
                            ElMessage({
                                message: '添加成功',
                                type: 'success'
                            });
                        } else {
                            console.error("Error submitting medicine data:", response.data.errorCode);
                            ElMessage.error('添加失败：' + response.data.errorCode)
                        }
                    })
                    .catch(error => {
                        console.error(error);
                        ElMessage.error('添加失败：' + error)
                    });
            }else{
                ElMessage.error('提交失败，请填写所有必填项');
            }
        }
    },
